export const metroMap = Object.fromEntries(metros.map((metro) => [metro.code, metro]));

export const metroDisplayName = (code) => {
  const metro = metroMap[code];
  return metro ? `${metro.city} (${metro.code})` : code;
};

export const airportDisplayName = (code) => {
  const metro = metroMap[code];
  return metro ? metro.airport : code;
};

export const metroSearchLabel = (code) => {
  const metro = metroMap[code];
  if (!metro) return code;
  return `${metro.city} (${metro.code})`;
};
